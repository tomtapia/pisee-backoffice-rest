package cl.gob.minsegpres.pisee.rest.entities.soap;

public class CampoOutputParameterSOAP {

	//-- attribs
	private String enArreglo;
	private String nombre;
	private String ruta;

	//-- getters
	public String getEnArreglo() {
		return enArreglo;
	}	
	public String getNombre() {
		return nombre;
	}	
	public String getRuta() {
		return ruta;
	}

	//-- setters
	public void setEnArreglo(String enArreglo) {
		this.enArreglo = enArreglo;
	}	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}	
	public void setRuta(String ruta) {
		this.ruta = ruta;
	}
	
}
